//Write a function that converts temperature from Celsius to Fahrenheit. The formula for conversion is: `F = (C * 9/5) + 32`.

function conversion(C) {
     let F = (C*9/5) + 32;
     return F;
 }
let Fahrenheit = conversion(6);

console.log("Fahrenheit:", Fahrenheit);
